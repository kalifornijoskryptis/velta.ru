		<h1>Познавательная информация о креслах</h1>
		<p>Кресло является мебельным изделием для сидения одного человека, со спинкой, с подлокотниками. Прообразы современных кресел появились ещё в <a href="#">Древнем Египте и Месопотамии</a>. При археологических раскопках на территории Месопотамии был обнаружен штандарт войны и мира, на котором мозаикой выложена сцена пиршества шумеров (III тысячелетие до н. э.). На этой сценке знатные гости сидят на невысоких церемониальных креслах. Спинка кресла достигала плеч сидевшего человека. Ножки кресла вырезаны в виде ног копытного животного, а с каждого бока между передней и задней ножками вставлены по две распорки, поддерживавшие сидение снизу.</p>
		<p>Первыми креслами были тронные кресла правителей и приближенных к ним лиц. Спинка тронного кресла египетского фараона Тутанхамона была отделана золотом, на самой спинке был изображён бог солнца Ра, а подлокотники выполнены в форме идущих львов.</p>
	</div>
	
