		<h3>Современность</h3>
		<p>Сегодня кресло — популярный предмет офисной мебели. Современные офисные стулья повышенной комфортности, которые могут называться креслами, делятся на обычные кресла и кресла руководителей. Ежегодно в мире продаётся около 70—100 миллионов офисных кресел, наибольшую долю занимают США (18 миллионов в год) и Германия (15 миллионов в год). Среднему покупателю офисного кресла на Западе примерно 40-45 лет, он разбирается в параметрах и технических возможностях кресла, у него есть проблемы со здоровьем и он достаточно обеспечен. В России в год продаётся 2,8 миллиона офисных кресел, а годовой объём продаж составляет 130 миллионов долларов.</p>
		<p>Круг материалов, используемых при изготовлении кресел, достаточно широк. Сами кресла изготавливаются из натурального дерева, ДСП, ротанга и других материалов. В качестве обивки применяются гобелен, флок, жаккард, шинилл, нубук, микрофибра. Для набивки используются синтетические и натуральные материалы: ватин, войлок, поролон, синтепон.</p>
